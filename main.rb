Reflecting on Principle
1) How are Ruby modules (and similar features from other langs)
   different from a class, in their purpose?
   
Modules allude to a commonality in the code when the 'is_a' type tests don't pass. Commonalities can be structured by modules to restrict hierarchies to narrow and shallow i.e. managable. Modules assist in extending code without messing it up.

2) What is LSP?

Liskov Substitution Principle essentially says that in order for type systems to be the same, subtypes must be substitutable for their supertypes.

3) Briefly describe how the template method pattern works.

Seperates abstract from concrete code wherein the "algorithms" are in the abstract portion.

